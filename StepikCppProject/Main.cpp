#include <iostream>
using namespace std;

/*	Шахматный король ходит по горизонтали, вертикали и диагонали, но только на 1 клетку.
	Даны две различные клетки шахматной доски, определите, может ли король попасть 
	с первой клетки на вторую одним ходом.

	Формат входных данных
	Программа получает на вход четыре числа от 1 до 8 каждое, задающие номер столбца 
	и номер строки сначала для первой клетки, потом для второй клетки.

	Формат выходных данных
	Программа должна вывести YES, если из первой клетки ходом короля можно попасть во вторую или NO в противном случае.

	Sample Input :
	4
	4
	5
	5

	Sample Output :
	YES
*/


int main(void) {
	int x1, y1;
	int x2, y2;

	cin >> x1 >> y1 >> x2 >> y2;

	bool result = abs(x1 - x2) <= 1 && abs(y1 - y2) <= 1;

	cout << (result ? "YES" : "NO");

	return 0;
}